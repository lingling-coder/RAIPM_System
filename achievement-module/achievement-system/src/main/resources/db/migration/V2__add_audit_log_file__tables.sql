CREATE TABLE audit_log (
    id BIGINT NOT NULL AUTO_INCREMENT,
    operator_id BIGINT COMMENT '操作人用户ID',
    operator_name VARCHAR(50) NOT NULL COMMENT '操作人用户名',
    operation_type VARCHAR(50) NOT NULL COMMENT 'LOGIN/LOGOUT/CREATE/UPDATE/DELETE',
    operation_name VARCHAR(100) NOT NULL COMMENT '操作描述，如"登录系统"/"新增用户-张三"',
    target_type VARCHAR(100) COMMENT '操作对象类型，如"User"/"Role"',
    target_id VARCHAR(100) COMMENT '操作对象ID',
    target_content LONGTEXT COMMENT '操作后内容(JSON)',
    original_content LONGTEXT COMMENT '操作前内容(JSON)，CREATE时为null',
    ip_address VARCHAR(50) COMMENT '客户端IP地址',
    user_agent VARCHAR(500) COMMENT '客户端User-Agent',
    status TINYINT DEFAULT 1 COMMENT '1=成功, 0=失败',
    previous_hash VARCHAR(64) NOT NULL DEFAULT '' COMMENT '上一条日志的SHA-256哈希值',
    current_hash VARCHAR(64) NOT NULL COMMENT '本条日志的SHA-256哈希值',
