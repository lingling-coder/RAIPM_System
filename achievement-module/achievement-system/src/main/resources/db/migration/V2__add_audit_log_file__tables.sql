-- -------------------------------------------------------------------------
-- file_record: file metadata for proxy-based file access
-- -------------------------------------------------------------------------
CREATE TABLE file_record (
    id BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    original_name VARCHAR(500) NOT NULL COMMENT '原始文件名',
    stored_name VARCHAR(100) NOT NULL UNIQUE COMMENT 'UUID存储文件名',
    storage_path VARCHAR(500) NOT NULL COMMENT '相对存储路径，如2026/06/paper/',
    file_size BIGINT NOT NULL COMMENT '文件大小(字节)',
    mime_type VARCHAR(100) COMMENT 'MIME类型',
    file_type VARCHAR(50) NOT NULL COMMENT '类型标识，如PAPER/PATENT/SOFTWARE/AVATAR',
    upload_ip VARCHAR(50) COMMENT '上传者IP',
    created_by BIGINT COMMENT '上传者用户ID',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_file_record_type (file_type),
    INDEX idx_file_record_created_at (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
