CREATE TABLE `notification` (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id BIGINT NOT NULL COMMENT '接收用户ID',
    type VARCHAR(50) NOT NULL COMMENT 'APPROVAL/SYSTEM',
    title VARCHAR(500) NOT NULL COMMENT '通知标题',
    content TEXT NULL COMMENT '通知内容',
    related_achievement_type VARCHAR(50) NULL COMMENT '关联成果类型',
    related_achievement_id BIGINT NULL COMMENT '关联成果ID',
    read_flag TINYINT DEFAULT 0 COMMENT '0=未读 1=已读',
    created_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_user_read (user_id, read_flag),
    INDEX idx_user_time (user_id, created_time),
    INDEX idx_created_time (created_time)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
PARTITION BY RANGE (YEAR(created_time) * 100 + MONTH(created_time)) (
